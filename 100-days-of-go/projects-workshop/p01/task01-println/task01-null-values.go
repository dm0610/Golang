package main

import (
	"fmt"
	"time"
)

func main() {
	var count int
	fmt.Printf("Count: %#v ", count)
	var discount float64
	fmt.Printf("Discount : %#v \n", discount)
	var debug bool
	fmt.Printf("Debug  : %#v \n", debug)
	var emails []string
	fmt.Printf("Emails   : %#v \n", emails)
	var startTime time.Time
	fmt.Printf("Start  : %#v \n", startTime)
}
